import { readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");
const imagesDir = path.join(rootDir, "public", "images");
const outputPath = path.join(rootDir, "src", "imageManifest.ts");

const entries = await readdir(imagesDir, { withFileTypes: true });
const files = entries
  .filter((entry) => entry.isFile())
  .map((entry) => entry.name)
  .filter((name) => /\.(png|jpg|jpeg|webp|gif)$/i.test(name))
  .sort((a, b) => a.localeCompare(b));

const lines = [
  "// This file is generated by scripts/generate-image-manifest.mjs.",
  "// Do not edit manually.",
  "export const IMAGE_FILES = [",
  ...files.map((name) => `  ${JSON.stringify(name)},`),
  "] as const;",
  "",
];

await writeFile(outputPath, lines.join("\n"), "utf8");
console.log(`Generated src/imageManifest.ts with ${files.length} image files.`);
